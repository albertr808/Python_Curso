frutas = ["manzana","piña","pera","sandia","uva","piña","durazno"]

frutas.insert(1,"cereza") #agregar elemento a un indice en especifico
frutas.append("naranja")          #agregar elemento al final de la lista
frutas.remove("piña")     #eliminar el primer elemento encontrado
frutas.pop()            #elimina el ultimo elemento o un indice especifico
del frutas[1]      #eliminar un elemento especifico
frutas.clear()     #elimina todos los elementos de un listado
print(frutas)
